<?php

class ModeleIdentifiantSite extends CI_Model

{


	/**********************************************************************
	**                           Constructeur                            **
	**********************************************************************/

	public function __construct()
	{
	    $this->load->database();
    }

	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/**************************                                                                              *************************************/
	/**************************                     SELECT () DE NOTRE TABLE GE_IDENTIFIANTSITE              *************************************/
	/**************************                                                                              *************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/

	public function rechercheSession($Email) 
	{
	   $this->db->select('*');
	   $this->db->from('ge_administrateur'); 
	   $this->db->where('Email', $Email);	  
	   $Liste = $this->db->get();
	   return $Liste->row();
	}
	  

	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/**************************                                                                              *************************************/
	/**************************                INSERT () DE NOTRE TABLE GE_IDENTIFIANTSITE                   *************************************/
	/**************************                                                                              *************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/

	public function insererInformationSession($donneesInserersession)
	{
	   return $this->db->insert('ge_administrateur', $donneesInserersession);
	}
 

	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/**************************                                                                              *************************************/
	/**************************                   UPDATE () DE NOTRE TABLE GE_IDENTIFIANTSITE                *************************************/
	/**************************                                                                              *************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/
	/*********************************************************************************************************************************************/

}